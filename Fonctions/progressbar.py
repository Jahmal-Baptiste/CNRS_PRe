import sys

def progressbar(it, prefix="", size=60, file=sys.stdout): #Just to plot the progress of some functions
    '''Prints a progress bar of size elements, during the loop it.'''
    count = len(it)
    def show(j):
        x = int(size*j/count)
        file.write("%s[%s%s] %i/%i\r" % (prefix, "#"*x, "."*(size-x), j, count))
        file.flush()        
    show(0)
    for i, item in enumerate(it):
        yield item
        show(i+1)
    file.write("\n")
    file.flush()